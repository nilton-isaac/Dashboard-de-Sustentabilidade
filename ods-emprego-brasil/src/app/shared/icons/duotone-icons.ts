// Função helper para renderizar ícones
export function renderIcon(iconName: keyof typeof DuotoneIcons, className: string = 'w-6 h-6'): string {
  const icon = DuotoneIcons[iconName];
  if (!icon) {
    console.warn(`Ícone '${iconName}' não encontrado`);
    return '';
  }
  
  return icon.replace('<svg', `<svg class="${className}"`);
}

// Função para obter apenas o SVG sem classes
export function getIcon(iconName: keyof typeof DuotoneIcons): string {
  return DuotoneIcons[iconName] || '';
}